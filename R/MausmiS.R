#' Square a given number
#'
#' Takes any numeric value and returns the square of this number.
#' @param x A numeric value to be squared.
#' @return The square of the number
#' @examples
#' squared_number <- square(c(5))
#' squared_number <- square(c(23))
#' @export

square <- function(x){
  return(x^2)
}

